	<p>
		The I Ching or Yi Jing (Chinese: 易經, Mandarin: [î tɕíŋ] (audio speaker iconlisten)), usually
		translated as Book of Changes or Classic of Changes, is an ancient Chinese divination text and
		among the oldest of the Chinese classics. Originally a divination manual in the Western Zhou
		period (1000–750 BC), over the course of the Warring States period and early imperial period
		(500–200 BC) it was transformed into a cosmological text with a series of philosophical
		commentaries known as the "Ten Wings".[1] After becoming part of the Five Classics in the 2nd
		century BCE, the I Ching was the subject of scholarly commentary and the basis for divination
		practice for centuries across the Far East, and eventually took on an influential role in
		Western understanding of Eastern thought.
	</p>
	<p>
		The I Ching is used in a type of divination called cleromancy, which uses apparently random
		numbers. Six numbers between 6 and 9 are turned into a hexagram, which can then be looked up in
		the text, in which hexagrams are arranged in an order known as the King Wen sequence. The
		interpretation of the readings found in the I Ching is a matter which has been endlessly
		discussed and debated over in the centuries following its compilation, and many commentators
		have used the book symbolically, often to provide guidance for moral decision making as informed
		by Confucianism, Taoism and Buddhism. The hexagrams themselves have often acquired cosmological
		significance and been paralleled with many other traditional names for the processes of change
		such as yin and yang and Wu Xing.
	</p>
